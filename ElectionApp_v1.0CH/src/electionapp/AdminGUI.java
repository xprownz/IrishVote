    private void loginBtnActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_loginBtnActionPerformed
        // TODO add your handling code here:

        String name; 
        String password; 
        String adminID; 
        
        Candidate a;

        name = nameTF.getText();
        password = passwordTF.getText();
        adminID = adminTF.getText();
        
        if(candidateRB.isSelected()){
            if(name.equals("") || password.equals("") || buttonGroup1.getSelection()==null){
                JOptionPane.showMessageDialog(null, "You must enter your Name and Password");
            }
            else{
                JOptionPane.showMessageDialog(null, "Hi "+name+", Login Successful");
                a = new Candidate(name, password);
                pollBtn.setVisible(true);
                voteBtn.setVisible(false);
                candidateBtn.setVisible(false);
                adm.add(a);
                nameTF.setText("");
                passwordTF.setText("");
            }
        }
        else if(adminRB.isSelected()){
            if(name.equals("") || password.equals("") || adminID.equals("") || buttonGroup1.getSelection()==null){
                JOptionPane.showMessageDialog(null, "You must enter your Name, Password and Admin ID");
            }
            else{
                JOptionPane.showMessageDialog(null, "Hi "+name+", Login Successful");
                adminID = adminTF.getText();
                a = new Admin(name, password, adminID);
                voteBtn.setVisible(true);
                candidateBtn.setVisible(true);
                pollBtn.setVisible(false);
                adm.add(a);
                nameTF.setText("");
                passwordTF.setText("");
                adminTF.setText("");
            }
        }
        else{
            JOptionPane.showMessageDialog(null, "Please fill in your details");
            a = new Candidate(name, password);
        }
       
        File outFile;
        FileWriter fw;
        BufferedWriter bw;
        
            try{
            outFile = new File("userLog.txt");
            fw = new FileWriter(outFile, true);
            bw = new BufferedWriter(fw);
            
            for(Candidate x: adm){
                if(x instanceof Candidate){
                    Candidate myCandidate = (Candidate) x;
                    bw.write(myCandidate.printDetails());
                    bw.newLine();
                }
                else if(x instanceof Admin){
                    Admin myAdmin = (Admin) x;
                    bw.write(myAdmin.printDetails());
                    bw.newLine();
                }
                else{
                    bw.write(x.printDetails());
                    bw.newLine();
                }

            }
            bw.close();
            
            
        } catch (IOException e){
            System.out.println("Error writing to file: "+ e);
        }
                    
      
    }//GEN-LAST:event_loginBtnActionPerformed

