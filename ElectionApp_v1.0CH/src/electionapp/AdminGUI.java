    private void candidateBtnActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_candidateBtnActionPerformed
        // TODO add your handling code here:
        String name;
        String occupation;
        String dob;
       
        File inFile;
        FileReader fr;
        BufferedReader br;
        
         try {
            inFile = new File("candidates.txt");
            fr = new FileReader(inFile);
            br = new BufferedReader(fr);
            
            name = br.readLine();
            occupation = br.readLine();
            dob = br.readLine();
            while (name != null) {
                JOptionPane.showMessageDialog(null, "Name: " + name +", Occupation: " + occupation+", Date of Birth: " +dob);
                name = br.readLine();
                occupation = br.readLine();
                dob = br.readLine();
            }
            br.close();
        } catch (IOException e){
            System.out.println(e);             
        } 
         
    }//GEN-LAST:event_candidateBtnActionPerformed

